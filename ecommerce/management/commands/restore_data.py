import sqlite3
from django.core.management.base import BaseCommand
from ecommerce.models import Category, Product, SliderImage
from django.contrib.auth.models import User

class Command(BaseCommand):
    help = "Restore data from old database"

    def handle(self, *args, **kwargs):
        self.stdout.write("Starting data restore...")

        # ── CATEGORIES ──
        categories_map = {}
        for cat in CATEGORIES:
            obj, created = Category.objects.get_or_create(
                slug=cat["slug"],
                defaults={"name": cat["name"]}
            )
            categories_map[cat["id"]] = obj
            if created:
                self.stdout.write(f"  Category created: {obj.name}")
            else:
                self.stdout.write(f"  Category exists: {obj.name}")

        # ── PRODUCTS ──
        for p in PRODUCTS:
            cat = categories_map.get(p["category_id"])
            obj, created = Product.objects.update_or_create(
                slug=p["slug"],
                defaults={
                    "name": p["name"],
                    "description": p["description"] or "",
                    "price": p["price"],
                    "image": p["image"],
                    "stock": p["stock"],
                    "is_featured": bool(p["is_featured"]),
                    "category": cat,
                }
            )
            status = "created" if created else "updated"
            self.stdout.write(f"  Product {status}: {obj.name}")

        # ── SLIDERS ──
        for s in SLIDERS:
            obj, created = SliderImage.objects.update_or_create(
                slide_type=s["slide_type"],
                defaults={
                    "title": s["title"],
                    "image": s["image"],
                    "is_active": bool(s["is_active"]),
                    "order": s["order"],
                }
            )
            self.stdout.write(f"  Slider: {obj.title}")

# ── USERS ──
        self.stdout.write("  Users skipped (already exist)")

        self.stdout.write(self.style.SUCCESS("Data restore complete!"))